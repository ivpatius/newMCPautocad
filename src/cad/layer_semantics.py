def get_layer_semantic(layer_name: str) -> str:
    """Возвращает семантическое описание слоя или fallback-строку."""
    # Прямое совпадение
    if layer_name in LAYER_SEMANTICS:
        return LAYER_SEMANTICS[layer_name]

    # Совпадение по префиксу (например, "АР_стены_несущие" → "АР_стены")
    for key in sorted(LAYER_SEMANTICS.keys(), key=len, reverse=True):
        if layer_name.startswith(key + "_") or layer_name == key:
            return LAYER_SEMANTICS[key]

    # fallback
    return f"Слой '{layer_name}' (не распознан)"


def get_hatch_material(pattern_name: str) -> str:
    """Возвращает описание материала по имени штриховки."""
    return HATCH_MATERIALS.get(pattern_name, f"Штриховка '{pattern_name}'")